<?php
$num1 = 10;
$num2 = 5;

// Addition
$sum = $num1 + $num2;
echo "Sum: " . $sum . "<br>";

// Subtraction
$diff = $num1 - $num2;
echo "Difference: " . $diff . "<br>";

// Multiplication
$prod = $num1 * $num2;
echo "Product: " . $prod . "<br>";

// Division
$quot = $num1 / $num2;
echo "Quotient: " . $quot . "<br>";

// Modulus (remainder)
$mod = $num1 % $num2;
echo "Modulus: " . $mod . "<br>";
?>